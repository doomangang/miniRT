#include "libft.h"

t_list	*ft_lstnew(char *content)
{
	t_list	*newnode;

	newnode = (t_list *)malloc(sizeof(t_list));
	if (!newnode)
		return (NULL);
	newnode->next = NULL;
	newnode->content = content;
	return (newnode);
}
